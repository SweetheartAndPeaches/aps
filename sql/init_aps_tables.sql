    FACTORY_CODE VARCHAR(20) NOT NULL COMMENT '工厂编号',
    MES_MATERIAL_CODE VARCHAR(30) DEFAULT NULL COMMENT 'MES物料编码',
    MATERIAL_CODE VARCHAR(30) NOT NULL COMMENT '物料编码',
    MATERIAL_DESC VARCHAR(256) DEFAULT NULL COMMENT '物料描述',
    CLASS_CAPACITY DECIMAL(8,0) DEFAULT NULL COMMENT '班产',
    MES_CAPACITY DECIMAL(8,0) DEFAULT NULL COMMENT 'MES实际产量',
    STANDARD_CAPACITY DECIMAL(8,0) DEFAULT NULL COMMENT '标准产能',
    APS_CAPACITY DECIMAL(8,0) DEFAULT NULL COMMENT 'APS计算日硫化量',
    SUM_VULCANIZATION DECIMAL(8,0) DEFAULT NULL COMMENT '硫化总时间(min)',
    VULCANIZATION_TIME DECIMAL(8,0) DEFAULT NULL COMMENT '硫化总时间(s)',
    MECHANICAL_TIME DECIMAL(8,0) DEFAULT '0' COMMENT '机械动作时(s)',
    CHECK_TIME DECIMAL(8,0) DEFAULT '0' COMMENT '点检时间',
    CLEAR_TIME DECIMAL(8,0) DEFAULT NULL COMMENT '检查胶囊、喷胶膜、模具清理2次',
    PRODUCTION_TIME DECIMAL(8,0) DEFAULT NULL COMMENT '生产作业时间(s)',
    STANDARD_TIME DECIMAL(8,0) DEFAULT NULL COMMENT '标准作业时间(s)',
    DINE_TIME DECIMAL(8,0) DEFAULT NULL COMMENT '就餐时间',
    REMARK VARCHAR(500) DEFAULT NULL COMMENT '备注',
    IS_DELETE CHAR(1) DEFAULT '0' COMMENT '是否删除：0-未删除，1-已删除',
    CREATE_TIME TIMESTAMP NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    UPDATE_TIME TIMESTAMP NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    CREATE_BY VARCHAR(20) DEFAULT NULL COMMENT '创建人',
    UPDATE_BY VARCHAR(20) DEFAULT NULL COMMENT '修改人',
    PRIMARY KEY (ID),
    KEY IDX_FACTORY_CODE (FACTORY_CODE),
