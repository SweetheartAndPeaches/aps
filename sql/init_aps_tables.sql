-- 1. T_CX_PARAM_CONFIG - 参数配置表
-- ---------------------------------------------
CREATE TABLE IF NOT EXISTS T_CX_PARAM_CONFIG (
    ID BIGINT(20) NOT NULL AUTO_INCREMENT COMMENT '主键ID',
    PARAM_CODE VARCHAR(50) NOT NULL COMMENT '参数编码',
    PARAM_NAME VARCHAR(100) NOT NULL COMMENT '参数名称',
    PARAM_VALUE VARCHAR(500) DEFAULT NULL COMMENT '参数值',
    GROUP_CODE VARCHAR(50) DEFAULT NULL COMMENT '分组编码',
    GROUP_NAME VARCHAR(100) DEFAULT NULL COMMENT '分组名称',
    SORT_ORDER INT DEFAULT 0 COMMENT '排序号',
    FACTORY_CODE VARCHAR(20) DEFAULT NULL COMMENT '工厂编码',
    IS_ACTIVE TINYINT(1) DEFAULT 1 COMMENT '是否启用：0-禁用，1-启用',
    IS_DELETE CHAR(1) DEFAULT '0' COMMENT '是否删除：0-未删除，1-已删除',
    CREATE_BY VARCHAR(64) DEFAULT NULL COMMENT '创建者',
    CREATE_TIME DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    UPDATE_BY VARCHAR(64) DEFAULT NULL COMMENT '更新者',
    UPDATE_TIME DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    PRIMARY KEY (ID),
    UNIQUE KEY UK_PARAM_CODE (PARAM_CODE, FACTORY_CODE)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='参数配置表';

